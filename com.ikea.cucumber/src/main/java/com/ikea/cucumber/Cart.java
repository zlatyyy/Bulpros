package com.ikea.cucumber;

public class Cart {
	private String id;
	private String quantity;
	private String opts;
	public String getId() {
	return id;
	}
	public void setId(String id) {
	this.id = id;
	}
	public String getQuantity() {
	return quantity;
	}
	public void setOpts(String opts) {
	this.opts = opts;
	}
	public String getOpts() {
	return opts;
	}
	public void setQuantity(String opts) {
	this.opts = opts;
	}
}
